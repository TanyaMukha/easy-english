/**
 * Get a random motivational quote
 * @returns Random quote from the collection
 */
export const getQuoteOfDayRandomly = (): Quote => {
  const randomIndex = Math.floor(Math.random() * MOTIVATIONAL_QUOTES.length);
  return MOTIVATIONAL_QUOTES[randomIndex];
};

/**
 * Get quote of the day based on current date (same quote for same day)
 * @returns Consistent quote for the current day
 */
export const getQuoteOfDay = (): Quote => {
  const today = new Date();
  const dayOfYear = Math.floor((today.getTime() - new Date(today.getFullYear(), 0, 0).getTime()) / 86400000);
  const quoteIndex = dayOfYear % MOTIVATIONAL_QUOTES.length;
  return MOTIVATIONAL_QUOTES[quoteIndex];
};